
public class ArithmeticOperators {

	public static void main(String[] args) {
		// +, -, *, /, %
		
		int a=90;
		int b=100;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		
		int c=10;
		int d=3;
		int sum=c+d;
		System.out.println(sum);
		
		int box=55;
		int box2=45;
		System.out.println(box+box2);
		
		

	}

}
