
public class VariableValues {

	public static void main(String[] args) {
		
		byte mika1=30;
		mika1=35;
		
		short mika2=130;
		mika2=145;
		
		int mika3=45000;
		mika3=48000;
		
		long mika4=250000000L;
		mika4=45528533200000L;
		
		
		float deni1=3.68F;
		double deni2=62.84;
		deni1=5.28F;
		deni2=58.26;
		
		char mia='M';
		char percent='%';
		mia='K';
		percent='&';
		
		boolean hungry=true;
		boolean happy=false;
		hungry=false;
		happy=true;
		
		
		
		
		System.out.println(mika1);
		System.out.println(mika2);
		System.out.println(mika3);
		System.out.println(mika4);
		
		System.out.println(deni1);
		System.out.println(deni2);
		
		System.out.println(mia);
		System.out.println(percent);
		System.out.println(hungry);
		System.out.println(happy);
	}

}
